#CREATE AGE GROUP 
ALTER TABLE application_record
ADD COLUMN Age_Group VARCHAR(20);
UPDATE application_record
SET Age_Group =
CASE
WHEN ABS(DAYS_BIRTH)/365 < 30 THEN 'Young'
WHEN ABS(DAYS_BIRTH)/365 BETWEEN 30 AND 50 THEN 'Mid Age'
ELSE 'Senior'
END;

#AGE GROUP DISTRIBUTION 
SELECT
Age_Group,
COUNT(*) AS Total_Customers
FROM application_record
GROUP BY Age_Group;

#CREATE INCOME CATEGORY 
ALTER TABLE application_record
ADD COLUMN Income_Category VARCHAR(20);

UPDATE application_record
SET Income_Category =
CASE
WHEN AMT_INCOME_TOTAL < 100000 THEN 'Low Income'
WHEN AMT_INCOME_TOTAL BETWEEN 100000 AND 300000 THEN 'Medium Income'
ELSE 'High Income'
END;

#INCOME CATEGORY DISTRIBUTION 
SELECT
Income_Category,
COUNT(*) AS Customers
FROM application_record
GROUP BY Income_Category;

#TOP CUSTOMER SEGMENT 
SELECT
Age_Group,
Income_Category,
COUNT(*) AS Customers
FROM application_record
GROUP BY Age_Group, Income_Category
ORDER BY Customers DESC;

# AVERAGE INCOME BY GENDER 
SELECT
CODE_GENDER,
AVG(AMT_INCOME_TOTAL) AS Avg_Income
FROM application_record
GROUP BY CODE_GENDER;

#AVERAGE INCOME BY EDUCTION 
SELECT
NAME_EDUCATION_TYPE,
AVG(AMT_INCOME_TOTAL) AS Avg_Income
FROM application_record
GROUP BY NAME_EDUCATION_TYPE
ORDER BY Avg_Income DESC;

#TOP OCCUPATION 
SELECT
OCCUPATION_TYPE,
COUNT(*) AS Employees
FROM application_record
GROUP BY OCCUPATION_TYPE
ORDER BY Employees DESC;
