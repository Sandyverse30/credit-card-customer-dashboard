CREATE TABLE application_record (
ID BIGINT,
CODE_GENDER VARCHAR(10),
FLAG_OWN_CAR VARCHAR(10),
FLAG_OWN_REALTY VARCHAR(10),
CNT_CHILDREN INT,
AMT_INCOME_TOTAL FLOAT,
NAME_INCOME_TYPE VARCHAR(100),
NAME_EDUCATION_TYPE VARCHAR(100),
NAME_FAMILY_STATUS VARCHAR(100),
NAME_HOUSING_TYPE VARCHAR(100),
DAYS_BIRTH INT,
DAYS_EMPLOYED INT,
FLAG_MOBIL INT,
FLAG_WORK_PHONE INT,
FLAG_PHONE INT,
FLAG_EMAIL INT,
OCCUPATION_TYPE VARCHAR(100),
CNT_FAM_MEMBERS FLOAT,
Age INT,
Age_Group VARCHAR(50),
Income_Category VARCHAR(50),
Experience_Years INT
);
